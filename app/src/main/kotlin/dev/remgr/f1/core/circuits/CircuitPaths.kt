    fun forCircuit(shortName: String): List<Offset>? =
        catalog[shortName] ?: catalog.entries
            .firstOrNull { shortName.contains(it.key, ignoreCase = true) }
            ?.value

    // ── Helper — vararg Float pairs → List<Offset> ────────────────────────────

    private fun o(vararg coords: Int): List<Offset> {
        require(coords.size % 2 == 0)
        return (0 until coords.size / 2).map { i ->
            Offset(coords[i * 2].toFloat(), coords[i * 2 + 1].toFloat())
        }
    }
